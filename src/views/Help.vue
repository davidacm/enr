<template>
  <div>
    <h1 aria-live="assertive">Instrucciones de uso.</h1>
    <h2>descripción de la aplicación.</h2>
    <p>
      Esta aplicación permite al usuario cargar un conjunto de notas, para luego
      leerlas mediante voz en orden consecutivo, con varios controles y métodos
      para la navegación entre las notas. Es una aplicación PWA, lo cual quiere
      decir que la puedes instalar en tu teléfono, computadora o tablet. Utiliza
      el botón "Instalar app" disponible en la pantalla principal de la
      aplicación.
    </p>
    <p>
      Fue creada con la finalidad de ayudar a las personas con discapacidad
      visual a guiarse cuando están realizando una presentación o leyendo algún
      discurso.
    </p>
    <p>
      Un ejemplo de uso sería que la persona abra la aplicación en el teléfono,
      escriba o cargue un archivo con sus notas, conecte sus auriculares por
      cable o bluetooth, y realice su presentación mientras navega y escucha las
      anotaciones cargadas en la aplicación.
    </p>
    <p>
      La navegación de notas se realiza mediante dos botones siguiente y
      anterior, o mediante gestos de deslizar hacia arriba y abajo en la
      pantalla. Puede que necesite deslizar con dos dedos si está usando
      Talkback. En Voice Over los gestos de deslizar no funcionarán, pero puede
      activar la respuesta de voz de la aplicación y desactivar temporalmente
      Voice Over si desea usar los gestos.
    </p>
    <p>
      Con esta aplicación el usuario puede estar seguro que el cursor siempre
      estará en la última nota leída, aunque toque cualquier parte de la
      pantalla, cambie de ventana o bloquee el teléfono. Además, los distintos
      métodos dispuestos para navegar entre las notas, hacen que el usuario
      pueda leer los mensajes con facilidad, seguridad y comodidad.
    </p>
    <h2>Descripción general de la interfaz.</h2>
    <p>Esta aplicación cuenta con tres pestañas:</p>
    <ol>
      <li>
        La pestaña de la izquierda es la que contiene los controles para leer
        las notas.
      </li>
      <li>
        La pestaña central posee los controles para escribir o cargar un archivo
        con notas, también contiene el botón para mostrar estas instrucciones.
      </li>
      <li>
        La pestaña de la derecha muestra las configuraciones de la aplicación.
      </li>
    </ol>
    <h2>Inicio rápido.</h2>
    <p>Para usar la aplicación, por favor siga los pasos a continuación:</p>
    <ol>
      <li>
        En la pantalla de inicio (pestaña central) cargue un archivo de texto o
        escriba texto en el campo de edición. Cada línea se convertirá en un
        mensaje.
      </li>
      <li>Vaya a la pestaña de notas (pestaña izquierda).</li>
      <li>
        utilice los botones siguiente o anterior para cambiar entre los mensajes
        guardados. Si está usando un dispositivo con pantalla táctil, puede usar
        los gestos deslizar hacia arriba o abajo para pasar entre los mensajes
        (Leer sección de gestos táctiles).
      </li>
    </ol>
    <h2>Instalar la aplicación.</h2>
    <p>
      Si desea tener esta aplicación en su escritorio o pantalla de inicio,
      puede usar el botón "Instalar app"que se encuentra en la pestaña central.
    </p>
    <p>
      Si instala la aplicación en su sistema, tendrá todo el espacio en la
      pantalla para la aplicación, ocultando los controles de su navegador. Esto
      podría mejorar la usabilidad.
    </p>
    <h2>¿Qué son las notas?</h2>
    <p>
      Las notas / mensajes son frases que serán leídas por la aplicación en un
      orden consecutivo y cíclico.
    </p>
    <p>
      Puede ingresar las notas en la aplicación mediante un archivo de texto,
      pegarlas desde el portapeles, o escribirlas usted mismo.
    </p>
    <p>
      La aplicación toma cada línea de texto como una nota independiente. Es
      decir, cada línea funcionaría como separador de las notas. Es necesario
      que tenga esto en cuenta al crear su archivo con anotaciones o al
      escribirlas en la aplicación. No es necesario enumerar las notas, pues la
      aplicación puede hacerlo por usted.
    </p>
    <p>
      Las últimas notas cargadas siempre serán guardadas en el sistema. La
      próxima vez que abra la aplicación, las notas más recientes estarán
      disponibles automáticamente.
    </p>
    <p>
      La aplicación puede aprovechar la respuesta de voz de su lector de
      pantallas para leer cada nota, o puede usar alguna voz instalada en el
      sistema si por algún motivo no desea usar un lector de pantallas.
    </p>
    <h2>Gestos táctiles.</h2>
    <p>
      Si cuenta con un dispositivo con pantalla táctil, puede usar los
      siguientes gestos para controlar la aplicación.
    </p>
    <ul>
      <li>
        Deslizar con un dedo hacia la izquierda o derecha: cambiar entre las
        pestañas de la aplicación.
      </li>
      <li>deslizar con un dedo hacia arriba: nota / mensaje anterior.</li>
      <li>
        deslizar con un dedo hacia abajo o derecha: nota / mensaje siguiente.
      </li>
    </ul>
    <p>
      Los gestos para pasar entre las notas, solo funcionan en la pantalla de
      notas.
    </p>
    <p>
      Si está usando un lector de pantallas, este podría impedir que los gestos
      táctiles lleguen a la aplicación. Si desea usar los gestos táctiles, se
      ofrecen las siguientes alternativas:
    </p>
    <ol>
      <li>
        Algunos lectores como Talkback para Android, permiten deslizar con dos
        dedos enviando el gesto de uno de los dedos a la aplicación. Puede
        intentar deslizar con dos dedos para todos los gestos de esta
        aplicación.
      </li>
      <li>
        Pedirle al lector que no capture el siguiente gesto. Esta acción se
        suele realizar con algún gesto preestablecido o configurado por el
        usuario. Requeriría que ejecute dos gestos cada vez que desee usar un
        gesto en esta aplicación.
      </li>
      <li>
        Desactivar el lector de pantallas. Si cuenta con un método fácil para
        alternar el estado de su lector, puede desactivarlo en los momentos en
        los que los gestos táctiles en esta aplicación sean más útiles para
        usted. Por ejemplo al leer las notas. Si opta por esta alternativa, debe
        activar la respuesta por voz en esta aplicación.
      </li>
    </ol>
    <h2>Control deslizante de navegación entre notas.</h2>
    <p>
      Este control, que puede ser activado en las opciones de configuración,
      ofrece una manera distinta de navegar por las notas. Al activarlo,
      sustituye los botones siguiente y anterior en la pantalla de notas.
    </p>
    <p>
      Si posiciona el foco del lector de pantallas en este control, puede usar
      funcionalidades del lector para cambiar entre las notas. Para hacerlo,
      utilice los gestos de su lector para cambiar el valor de este control.
    </p>
    <p>
      Por ejemplo en Voice Over de iOS (iPhone) con este control enfocado, puede
      usar los gestos deslizar arriba y abajo para cambiar entre las notas.
    </p>
    <p>
      Si está usandoAndroid, con este control enfocado, puede usar los botones
      de volumen para navegar entre las notas.
    </p>
    <p>
      Algunas veces los lectores de pantalla pueden leer información relacionada
      con el valor actual del control al cambiar su valor. Podría resultar un
      poco incómodo, pero no hay ninguna solución conocida por ahora.
    </p>
    <p>
      El valor actual del control no tiene ninguna utilidad,simplemente, permite
      a la aplicación detectar si el usuario desea leer la nota siguiente o
      anterior. Para evitar que el control llegue a un punto donde no pueda ser
      disminuido o incrementado, se evitará que se establezca en el valor mínimo
      o máximo.
    </p>
    <h2>Información sobre las pantallas de la aplicación.</h2>
    <h3>Pantalla de notas.</h3>
    <p>
      En esta pantalla se muestran los controles para avanzar o retroceder por
      los mensajes cargados en la aplicación y algunas opciones de
      funcionamiento.
    </p>
    <p>
      Los botones para avanzar o retroceder se encuentran en la parte inferior
      de la pantalla, abarcando todo el espacio inferior para que sean fáciles
      de ubicar en los dispositivos táctiles como teléfonos inteligentes. El
      botón anterior se encuentra en la esquina inferior izquierda, el botón
      siguiente en la esquina inferior derecha.
    </p>
    <p>
      Encima de los botones anterior y siguiente, se ubica un botón llamado
      iniciar. Al activar este botón, impedirá que la pantalla del dispositivo
      se bloquee. De esta manera, no tendrás que desbloquear tu dispositivo
      cuando requieras leer el próximo mensaje.
    </p>
    <p>
      En la parte superior de la pantalla, existen dos controles alternables, su
      estado puede ser cambiado en cualquier momento.
    </p>
    <ul>
      <li>
        Enumerar notas: asignará un número consecutivo a cada mensaje / nota.
      </li>
      <li>
        Usar síntesis de voz del sistema: permite usar la síntesis del sistema,
        se recomienda configurar el idioma y la voz previamente en las opciones
        de configuración.
      </li>
    </ul>
    <p>Aclaraciones:</p>
    <ul>
      <li>
        si no existen notas cargadas, solo se mostrará un mensaje que dice "no
        hay notas".
      </li>
      <li>
        Las notas se muestran de forma consecutiva y cíclica. Si la nota actual
        es la última y pasa a la siguiente, se leerá la primera nota en su
        lugar.
      </li>
      <li>
        el botón para impedir el bloqueo de la pantalla puede que no funcione en
        todos los dispositivos.
      </li>
      <li>
        los botones anterior y siguiente pueden sustituirse por un control
        deslizante, ver opciones de configuración.
      </li>
      <li>
        Si activó el botón "Usar síntesis de voz del sistema" y no recibe
        respuesta de voz, es probable que la voz necesite ser configurada
        previamente, o que su sistema no tenga soporte para síntesis de voz.
      </li>
      <li>
        Si activa la respuesta de voz y el lector de pantallas está activo, las
        notas serán leídas por el lector y la voz de la aplicación al mismo
        tiempo.
      </li>
    </ul>
    <h3>Pantalla de inicio (central).</h3>
    <p>
      En la pantalla de inicio puede cargar un archivo de texto con las notas.
      Al activar el botón para seleccionar el archivo, se mostrará un diálogo de
      sistema que le permitirá navegar hasta donde se encuentra su archivo de
      notas.
    </p>
    <p>
      Esta pantalla posee también un campo de edición multilínea, en el cual
      podrás escribir tus notas o pegarlas desde el portapeles.
    </p>
    <p>
      En la parte superior se ubican los botones para instalar la aplicación en
      el sistema, un botón por si deseas hacerme una donación, y el botón para
      mostrar estas instrucciones.
    </p>
    <p>
      El botón para instalar la aplicación se ocultará si la aplicación es
      instalada en el sistema.
    </p>
    <h3>Pantalla de configuración.</h3>
    <p>Aquí se configura el comportamiento de la aplicación.</p>
    <ul>
      <li>Enumerar notas: asigna un número consecutivo a cada nota.</li>
      <li>
        Usar control deslizante para moverse por las notas: sustituye los
        botones anterior y siguiente de la pantalla de notas.
      </li>
      <li>
        Usar síntesis de voz del sistema: permite que la aplicación lea las
        notas con alguna voz que se haya configurado, si el sistema cuenta con
        voces instaladas.
      </li>
    </ul>
    <h4>Configuración de voz.</h4>
    <p>
      Si se activa la opción "Usar síntesis de voz del sistema", se añadirán las
      siguientes opciones en la pantalla de configuración:
    </p>
    <ul>
      <li>idioma de la voz: el idioma usado para leer las notas.</li>
      <li>
        voz para leer notas: la voz correspondiente al idioma seleccionado, para
        leer las notas.
      </li>
      <li>velocidad: la velocidad de la voz.</li>
      <li>tono: el tono de la voz, más grave o más agudo.</li>
      <li>
        Mensaje de prueba: puede escribir un texto en dicho campo o dejar el
        texto por defecto, para probar la voz seleccionada.
      </li>
      <li>
        probar voz: al activar este botón, se leerá el texto escrito en el campo
        anterior, con la voz y el idioma seleccionados.
      </li>
      <li>
        Guardar configuraciones de voz: guardará las configuraciones de voz, de
        manera que se utilizará el mismo idioma y la misma voz al iniciar la
        aplicación nuevamente.
      </li>
    </ul>
